export type Locale = keyof typeof translations;
export type TranslationKeys = typeof translations.en;

export function getTranslations(locale: Locale = 'en'): TranslationKeys {
  return translations[locale] || translations.en;
}

// Helper function to detect locale from hostname
export function getLocaleFromHostname(hostname: string): Locale {
  if (hostname.startsWith('pl.')) {
    return 'pl';
  }
  return 'en';
}

// Helper function to detect locale from pathname (for localhost/development)
export function getLocaleFromPathname(pathname: string): Locale {
  if (pathname.startsWith('/pl')) {
    return 'pl';
  }
  return 'en';
}

// Currency conversion (approximate)
export const CURRENCY_RATES = {
  en: 1,
  pl: 4, // 1 USD ≈ 4 PLN (approximate, should be updated regularly)
};

export function convertPrice(priceUSD: number, locale: Locale): number {
  return Math.round(priceUSD * CURRENCY_RATES[locale]);
}
